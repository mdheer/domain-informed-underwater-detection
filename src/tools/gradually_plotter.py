import os
import cv2
import numpy as np
import matplotlib.pyplot as plt


def create_video_from_folder(
    folder_path, points_list, output_file="output_video.avi", fps=30
):
    images = sorted(os.listdir(folder_path))
    img = cv2.imread(os.path.join(folder_path, images[0]))
    height, width, _ = img.shape
    size = (width, height)

    fig, ax = plt.subplots()
    plt.xlim(0, width)
    plt.ylim(height, 0)

    out = cv2.VideoWriter(output_file, cv2.VideoWriter_fourcc(*"DIVX"), fps, size)

    for i, image_file in enumerate(images):
        img = cv2.imread(os.path.join(folder_path, image_file))

        # Plotting points
        if i < len(points_list):
            for point in points_list[i]:
                ax.plot(point[0], point[1], "ro")
            plt.draw()

            # Convert the plot to a numpy array
            plt_frame = np.frombuffer(fig.canvas.tostring_rgb(), dtype=np.uint8)
            plt_frame = plt_frame.reshape(fig.canvas.get_width_height()[::-1] + (3,))
            plt_frame = cv2.cvtColor(plt_frame, cv2.COLOR_RGB2BGR)

            # Combine the image and plot horizontally
            combined_frame = np.hstack((img, plt_frame))

            # Write the combined frame to the output video
            out.write(combined_frame)

    plt.close(fig)
    out.release()


